<p><strong>Zakat can only be paid to specific beneficiaries and projects.</strong>&nbsp;</p>



<p>The Qur’an mentions eight groups of people on who Zakat should be spent:</p>



<p><em>(Surat At-Tawbah 9:60)</em></p>



