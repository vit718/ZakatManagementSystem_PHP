<p><strong>Silver</strong>: For example, if the price of silver is £1 a gram, the Nisaab (£1 x 612.36) on which Zakat is due is £612.36. So anyone who has £612.36 or more in savings will have to pay 2.5% Zakat on that (minimum Zakat will be £15.31).</p>
