<p><strong>Gold</strong>: For example, if the price of gold is £20 a gram, the Nisaab (£20 x 87.48) on which Zakat is due is £1,749. So anyone who has £1,749 or more in savings will have to pay 2.5% Zakat on that (minimum Zakat will be £43.73).</p>
