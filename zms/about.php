<div class="wp-block-column">
<h3><span style="color: black;" class="fc-red">Why do we give Zakat?</span></h3>



<p>Zakat is not charity, It is not voluntary charity not a tax but an obligation. By giving Zakat, a Muslim is acknowledging that everything we have is Allah’s and we do not really own it, and we should use it to remember Allah and help those who are in need. It is also an act to help free us from excessive desire and greed, learn self-discipline and honesty.<br></p>
