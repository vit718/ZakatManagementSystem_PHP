<h3><span style="color: black;" class="fc-red">Should I use gold or silver to calculate Nisaab?</span></h3>



<p>While it is permissible to use either gold or silver to calculate Nisaab, many scholars recommend using silver as more people paying Zakat means more of the poor and needy benefit.&nbsp;<br></p>



