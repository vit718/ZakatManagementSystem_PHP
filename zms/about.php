<div class="wp-block-column">
<h3><span style="color: black;" class="fc-red">When should we give Zakat?</span></h3>



<p>You should record the Islamic date on which you first became the owner of Nisaab &#8211; if, on the same date the following lunar year, you are still the owner of Nisaab, you will be obliged to pay Zakat. Many Muslims choose to give it during Ramadan as the rewards for good deeds performed in Ramadan are greater than in any other month.<br></p>
