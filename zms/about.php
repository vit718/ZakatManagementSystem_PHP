<p>Zakat, the giving of alms to the poor and needy, is one of the five pillars of Islam (the others are declaration of faith, prayer, fasting in Ramadan and Hajj). It&nbsp;is obligatory upon every adult Muslim of sound mind and means.</p>



<p>The individual must own a specific amount of wealth or savings (after living costs, expenses etc). This is referred to as Nisaab and is the threshold at which Zakat becomes payable. The amount of Zakat to be paid is 2.5% of Nisaab .</p>



<p style="color:#7bc32d" class="has-text-color">&#8220;<em>The alms are only for the Fuqara&#8217; (the poor), and Al-Masakin (the needy) and those employed to collect (the funds); and to attract the hearts of those who have been inclined (towards Islam); and to free the captives; and for those in debt; and for Allah&#8217;s Cause, and for the wayfarer (a traveler who is cut off from everything); a duty imposed by Allah. And Allah is All-Knower, All-Wise.</em>&#8221;&nbsp;[Al-Quran 9:60]</p>



<p style="color:#7bc32d" class="has-text-color">&#8220;<em>They ask you as to what they should spend. Say: Whatever wealth you spend, it is for the parents and the near of kin and the orphans and the needy and the wayfarer, and whatever good you do, Allah surely knows it.</em>&#8221;&nbsp;[Al-Quran 2:215]</p>



<p>&#8220;<em>(Alms are) for the poor who are confined in the way of Allah&#8211; they cannot go about in the land; the ignorant man thinks them to be rich on account of (their) abstaining (from begging); you can recognize them by their mark; they do not beg from men importunately; and whatever good thing you spend, surely Allah knows it.</em>&#8221; [Al-Quran 2:273]&nbsp;</p>



