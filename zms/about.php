<h3><span style="color: black;" class="fc-red">How is Zakat calculated?</span></h3>



<p>Assets to include in your Zakat calculation are cash (in hand, in bank accounts or money lent to someone), shares, pensions, gold and silver. Personal items (your home, furniture, cars, food, clothing) are not included in Nisaab.</p>



<p>There are two measures to determine Nisaab – gold (3 ounces or 87.48 grams, or its cash equivalent) or silver (21 ounces or 612.36 grams, or its cash equivalent).</p>



<p>Using today&#8217;s (16 August 2019) value of gold (87.48 grams), the nisaab is £3,501.82</p>



<p>Using today&#8217;s value of silver (612.36 grams), the nisaab is £278.01</p>



